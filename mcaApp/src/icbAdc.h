/* Definitions for Canberra 9635 ADC */

/* Author:  Mark Rivers 
 * Date:    October 4, 2000 
 */

#define ID_ADC_CGAIN    1 /* Conversion gain */
#define ID_ADC_CRANGE   2 /* Conversion range */
#define ID_ADC_OFFSET   3 /* Digital offset */
#define ID_ADC_LLD      4 /* Lower-level discriminator */
#define ID_ADC_ULD      5 /* Upper-level discriminator */
#define ID_ADC_ZERO     6 /* Zero offset */
#define ID_ADC_GMOD     7 /* Gate mode */
#define ID_ADC_CMOD     8 /* Coincidence mode */
#define ID_ADC_PMOD     9 /* Peak-detect mode */
#define ID_ADC_AMOD    10 /* Acquisition mode */
#define ID_ADC_TMOD    11 /* Data transfer mode */
